    public pelangganView() {
        initComponents();
        
        plgServis = new pelangganImplement();
        tablePelanggan.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent lse) {
                row = tablePelanggan.getSelectedRow();
                if (row!=-1) {
                    isiText();
                }
            }
        });
        this.statusAwal();
    }
    
    void loadData(){
        try {
            record = plgServis.getAll();
        } catch (SQLException ex) {
            Logger.getLogger(pelangganView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    void isiTabel(){
        Object data[][] = new Object[record.size()][5];
        int x = 0;
        for (Pelanggan plg : record) {
            data[x][0]=plg.getIdPelanggan();
            data[x][1]=plg.getNama();
            data[x][2]=plg.getJeniskel();
            data[x][3]=plg.getAlamat();
            data[x][4]=plg.getNotlp();
            x++;
        }
        String judul[]={"ID Pelanggan","Nama","Jenis Kelamin","Alamat","No Telepon"};
        tablePelanggan.setModel(new DefaultTableModel(data, judul));
        jScrollPane1.setViewportView(tablePelanggan);
        
    }
    
    void isiText(){
        Pelanggan plg=record.get(row);
        txtIdPelanggan.setText(plg.getIdPelanggan());
        txtNama.setText(plg.getNama());
         if(plg.getJeniskel().equals("Laki-laki")){
            radioLakilaki.setSelected(true);
            radioPerempuan.setSelected(false);
        }else{
            radioPerempuan.setSelected(true);
            radioLakilaki.setSelected(false);
        }
        txtalamat.setText(plg.getAlamat());
        txtnotlp.setText(plg.getNotlp());
    }
    
    void kosongkanText(){
        txtIdPelanggan.setText("");
        txtNama.setText("");
        txtalamat.setText("");
        txtnotlp.setText("");
    }
    
    void statusAwal(){
        kosongkanText();
        loadData();
        isiTabel();
    }


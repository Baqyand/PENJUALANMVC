    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
         try{
            Pelanggan pelanggan = new Pelanggan();
            pelanggan.setIdPelanggan(txtIdPelanggan.getText());
            pelanggan.setNama(txtNama.getText());
            if(radioLakilaki.isSelected()){
                pelanggan.setJeniskel("Laki-laki");
            }else if(radioPerempuan.isSelected()){
                pelanggan.setJeniskel("Perempuan");
            }
            pelanggan.setAlamat(txtalamat.getText());
            pelanggan.setNotlp(txtnotlp.getText());
            plgServis.insert(pelanggan);
            this.statusAwal();
            JOptionPane.showMessageDialog(this, "Data Berhasil Ditambahkan");
        }catch(SQLException ex){
            Logger.getLogger(pelangganView.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex);
        }  
    }//GEN-LAST:event_btnTambahActionPerformed

